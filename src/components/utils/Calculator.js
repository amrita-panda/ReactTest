const mathOperations={
    sum:function(x,y){
        return x+y;
    },
    substract:function(x,y){
        return x-y;
    },
    divide:function(x,y){
        return x/y;
    },
    multiply:function(x,y){
        return x*y;
    },
    module:function(x,y){
        return x%y;
    }
}
module.exports=mathOperations;